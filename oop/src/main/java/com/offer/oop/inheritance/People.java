package com.offer.oop.inheritance;

public class People {
    private String name;
    private int age;


    public void   eat(){
        System.out.println("my name is :"+name+" i am eat");
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "People{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
